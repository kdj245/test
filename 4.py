
# 불리언 3rd- 조건문 if else, elif, else
a = 3
b = 2
c = 5
d = c - a

if a > b:        #a가 b보다 크면 Hello
    print("Hello")
if a > c:        #a가 c보다 크면 What?
    print("What?")
elif a == d:     #a가 3과 같다면 bye~!
    print("Bye~!")
else:            #a가 b보다 작으면 Hi
    print("Hi~!")

